class CountryCodes:

    def __init__(self):
        return

    # Get country by code
    def getCountry(self,code):
        country = ''
        code = code.upper()
        try:
            ucCountry = ISO3166[code]
            for word in ucCountry.split():
                word = word.capitalize()
                country = country + ' ' + word
            country = country.lstrip()
            country = country.rstrip()
        except:
            country = 'Not found'
        return country

    def printList(self):
        for x, y in ISO3166.items():
            country = "%s %s " % (x,y)
            print(country)

    def getList(self):
        return ISO3166

